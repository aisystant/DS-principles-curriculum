def make_concepts_file(section_num, data):
    lines = [
        "---",
        "wp: 300",
        f"guide: 1",
        f"section: S{section_num}",
        f"subsection_id: PD.GUIDE.1.S{section_num}.SS8",
        "title: Список понятий раздела",
        "order: 8",
        "introduces: []",
        "uses: []",
        "prerequisites: []",
        "mastery_node: []",
        "stage_relevant: []",
        "can_do: []",
        "cp_check: []",
        "bh_check: []",
        "time_reading: 10 min",
        "status: draft",
        "format_version: 4.1-aux",
        "---",
        "",
        f"#### §{section_num}.08 Список понятий раздела",
        "",
        "Сводная таблица всех понятий, введённых в Разделе {}. Используйте её для проверки: можете ли вы объяснить каждое определение своими словами, не заглядывая в текст?".format(section_num),
        "",
        "| Понятие | Краткое определение | Введено |",
        "|---------|---------------------|---------|",
    ]
    for concept, definition, source in data["concepts"]:
        lines.append(f"| **{concept}** | {definition} | {source} |")
    lines.append("")
    return "\n".join(lines)


